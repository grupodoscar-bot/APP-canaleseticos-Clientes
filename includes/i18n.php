function current_lang(): string {
    if (!empty($_GET['lang']) && in_array($_GET['lang'], ['es','en'], true)) {
        $_SESSION['lang'] = $_GET['lang'];
    }
    if (!empty($_SESSION['lang']) && in_array($_SESSION['lang'], ['es','en'], true)) return $_SESSION['lang'];
    try {
        $def = db()->query("SELECT idioma_default FROM tenants_config WHERE id=1")->fetchColumn();
        if (in_array($def, ['es','en'], true)) return $def;
    } catch (Throwable $e) {}
    // Auto-detect browser
    if (!empty($_SERVER['HTTP_ACCEPT_LANGUAGE']) && preg_match('/^(es|en)/i', $_SERVER['HTTP_ACCEPT_LANGUAGE'], $m)) {
        return strtolower($m[1]);
    }
    return 'es';
}
function t(string $key, string $fallback = ''): string {
    $lang = current_lang();
    return $GLOBALS['I18N'][$lang][$key] ?? ($GLOBALS['I18N']['es'][$key] ?? ($fallback ?: $key));
}
function tr(string $key): string { return t($key); } // alias
